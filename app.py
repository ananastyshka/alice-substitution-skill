def normalize_unit(text):
    """Нормализует единицу измерения из произвольного текста."""
    text = text.lower().strip()
    for syn, norm in sorted(UNIT_SYNONYMS.items(), key=lambda x: -len(x[0])):
        if syn in text:
            return norm
    return None


def find_product_in_density(text):
    """Ищет название продукта в DENSITY по тексту запроса.
    Нормализует токены через DECLENSION_MAP (молока→молоко) перед поиском."""
    text_lower = text.lower()
    # Нормализуем падежи
    tokens = [DECLENSION_MAP.get(t, t) for t in re.sub(r'[,.!?]+', ' ', text_lower).split()]
    text_norm = ' '.join(tokens)
    for prod in sorted(DENSITY.keys(), key=len, reverse=True):
        if prod in text_norm:
            return prod
    for prod in sorted(DENSITY.keys(), key=len, reverse=True):
        if prod in text_lower:
            return prod
    return None


def handle_conversion(user_input):
    """
    Конвертирует кулинарные единицы.
    Поддерживает запросы вида:
      «2 столовые ложки молока это сколько грамм»
      «200 г муки в столовые ложки»
      «сколько грамм в стакане муки»
      «переведи 2 чайные ложки соли в граммы»
    """
    text = user_input.lower().strip()
    # Убираем знаки пунктуации, но сохраняем точки (нужны для «ст.л.», «ч.л.»)
    text = re.sub(r'[,!?;:«»"\'()]+', ' ', text)

    # Проверяем штучные продукты
    for prod in UNCOUNTABLE_PRODUCTS:
        pat = r'\b' + re.escape(prod) + r'\b'
        if re.search(pat, text):
            w = PIECE_WEIGHTS.get(prod)
            if w:
                return (
                    f'«{prod}» — штучный продукт, перевести в граммы по объёму нельзя. '
                    f'Для справки: 1 среднее {prod} весит примерно {w} г.'
                )
            return f'«{prod}» — штучный продукт, его нельзя однозначно перевести без взвешивания.'

    # Числовое количество
    qty_match = re.search(r'(\d+(?:[.,]\d+)?)', text)
    qty = float(qty_match.group(1).replace(',', '.')) if qty_match else None

    # Ищем продукт через нормализацию падежей
    product = find_product_in_density(text)

    # Определяем единицы через regex с границами слов
    # Порядок: длинные синонимы раньше, чтобы «столовые ложки» не разбивалось на «л»+«ложки»
    ORDERED_PATTERNS = [
        # многословные — первыми
        (r'столов\w+\s+ложк\w+', 'ст.л.'),
        (r'ст\s+л\b', 'ст.л.'),  # «ст л» без точек
        (r'ч\s+л\b', 'ч.л.'),   # «ч л» без точек
        (r'чайн\w+\s+ложк\w+', 'ч.л.'),
        (r'столов\w+\s+ложках', 'ст.л.'),
        (r'ст\.\s*л\.?', 'ст.л.'),
        (r'ч\.\s*л\.?', 'ч.л.'),
        (r'стакан\w*', 'стакан'),
        (r'щепотк\w+', 'щепотка'),
        (r'зубчик\w*', 'зубчик'),
        (r'миллилитр\w*', 'мл'),
        (r'мл\b', 'мл'),
        (r'килограмм\w*', 'кг'),
        (r'кг\b', 'кг'),
        (r'литр\w*', 'л'),
        # граммы — после литров и миллилитров
        (r'граммов|грамма|граммах|граммы|грамм\b', 'г'),
        (r'\bгр\b', 'г'),
        (r'\bг\b', 'г'),
        # литр однобуквенный — последним (конфликтный)
        (r'\bл\b', 'л'),
    ]

    found_units = []  # list of (pos, end, norm_unit)
    covered = []      # занятые диапазоны

    for pat, norm_u in ORDERED_PATTERNS:
        for m in re.finditer(pat, text):
            pos, end = m.start(), m.end()
            if any(not (end <= cs or pos >= ce) for cs, ce in covered):
                continue
            if any(u == norm_u for _, _, u in found_units):
                continue
            found_units.append((pos, end, norm_u))
            covered.append((pos, end))

    found_units.sort(key=lambda x: x[0])
    all_units = [u for _, _, u in found_units]

    # Эвристика «сколько X в Y»: X — цель, Y — источник
    m_skolko = re.search(
        r'сколько\s+([\w\s.]+?)\s+(?:в|в одном)\s+([\w\s.]+?)(?:\s+|$)',
        text
    )
    if m_skolko and len(all_units) >= 2:
        t_str = m_skolko.group(1).strip()
        s_str = m_skolko.group(2).strip()
        tu = _match_unit(t_str, ORDERED_PATTERNS)
        su = _match_unit(s_str, ORDERED_PATTERNS)
        if tu and su and tu != su:
            from_unit, to_unit = su, tu
        else:
            from_unit = all_units[0]
            to_unit   = all_units[1] if len(all_units) > 1 else None
    elif len(all_units) >= 2:
        from_unit, to_unit = all_units[0], all_units[1]
    elif len(all_units) == 1:
        from_unit, to_unit = all_units[0], None
    else:
        from_unit, to_unit = None, None

    qty_val = qty or 1.0

    # Простые единичные переводы
    if from_unit == 'кг' and to_unit in ('г', None):
        return f'{qty_val:g} кг = {round(qty_val * 1000)} г.'
    if from_unit == 'г' and to_unit == 'кг':
        return f'{qty_val:g} г = {qty_val / 1000:g} кг.'
    if from_unit == 'л' and to_unit in ('мл', None):
        return f'{qty_val:g} л = {round(qty_val * 1000)} мл.'
    if from_unit == 'мл' and to_unit == 'л':
        return f'{qty_val:g} мл = {qty_val / 1000:g} л.'

    # Нет продукта
    if not product:
        # Стакан без продукта
        if from_unit == 'стакан' and to_unit in ('мл', 'г', None):
            ml = round(qty_val * 200)
            return f'{qty_val:g} стакан(а) = {ml} мл ≈ {ml} г воды. Для точного результата уточните продукт: «{qty_val:g} стакан муки это сколько г?»'
        if to_unit == 'стакан' and from_unit in ('мл', 'г', None):
            cups = round(qty_val / 200, 1)
            return f'{qty_val:g} {from_unit or "ед."} ≈ {cups:g} стакана (200 мл).'
        if from_unit and to_unit:
            return (
                f'Для точного перевода уточните продукт. '
                f'Например: «{qty_val:g} {from_unit} муки это сколько {to_unit}?»'
            )
        return None

    tbl = DENSITY.get(product, {})

    # объём/вес → граммы
    if from_unit in ('ст.л.', 'ч.л.', 'стакан', 'щепотка') and to_unit in ('г', None):
        range_key = (product, from_unit)
        if range_key in UNIT_RANGES:
            lo, hi = UNIT_RANGES[range_key]
            lo_t, hi_t = round(qty_val * lo), round(qty_val * hi)
            extra = ''
            if tbl.get('мл') and from_unit == 'ст.л.':
                extra = f' (или ≈ {round(qty_val * 15)} мл)'
            return f'{qty_val:g} {from_unit} {product} — это примерно {lo_t}–{hi_t} г{extra}.'
        g_per = tbl.get(from_unit) or DEFAULT_UNIT_G.get(from_unit)
        if g_per:
            total = round(qty_val * g_per)
            extra = ''
            if tbl.get('мл') and from_unit == 'ст.л.':
                extra = f' (или ≈ {round(qty_val * 15)} мл)'
            return f'{qty_val:g} {from_unit} {product} — это примерно {total} г{extra}.'
        return f'Нет данных о весе «{product}» на {from_unit}.'

    # мл → граммы
    if from_unit == 'мл' and to_unit in ('г', None):
        g_ml = tbl.get('мл', 1.0)
        return f'{qty_val:g} мл {product} — это примерно {round(qty_val * g_ml)} г.'

    # граммы → ст.л.
    if from_unit == 'г' and to_unit in ('ст.л.', None):
        g_tbsp = tbl.get('ст.л.')
        if g_tbsp:
            tbsp = round(qty_val / g_tbsp, 1)
            return f'{qty_val:g} г {product} — это примерно {tbsp:g} ст.л.'
        return f'Нет данных о ложках для «{product}».'

    # граммы → ч.л.
    if from_unit == 'г' and to_unit == 'ч.л.':
        g_tsp = tbl.get('ч.л.')
        if g_tsp:
            tsp = round(qty_val / g_tsp, 1)
            return f'{qty_val:g} г {product} — это примерно {tsp:g} ч.л.'
        return f'Нет данных о чайных ложках для «{product}».'

    # граммы → стакан
    if from_unit == 'г' and to_unit == 'стакан':
        g_cup = tbl.get('стакан')
        if g_cup:
            cups = round(qty_val / g_cup, 2)
            return f'{qty_val:g} г {product} — это примерно {cups:g} стакана (200 мл).'
        return f'Нет данных о стаканах для «{product}».'

    # граммы → мл
    if from_unit == 'г' and to_unit == 'мл':
        g_ml = tbl.get('мл')
        if g_ml:
            return f'{qty_val:g} г {product} — это примерно {round(qty_val / g_ml)} мл.'
        return f'Нет данных о плотности «{product}».'

    # стакан → г или мл
    if from_unit == 'стакан':
        g_cup = tbl.get('стакан')
        ml_val = round(qty_val * 200)
        if g_cup:
            g_total = round(qty_val * g_cup)
            if to_unit == 'мл':
                return f'{qty_val:g} стакан(а) {product} — это примерно {ml_val} мл.'
            return f'{qty_val:g} стакан(а) {product} — это примерно {g_total} г (или {ml_val} мл).'
        if to_unit == 'мл':
            return f'{qty_val:g} стакан(а) {product} — это примерно {ml_val} мл.'
        return f'Нет данных о плотности «{product}» по стаканам.'

    # ст.л./ч.л. → мл
    if from_unit in ('ст.л.', 'ч.л.') and to_unit == 'мл':
        ml_per = 15.0 if from_unit == 'ст.л.' else 5.0
        g_ml = tbl.get('мл', 1.0)
        total_ml = round(qty_val * ml_per)
        total_g  = round(qty_val * ml_per * g_ml)
        return f'{qty_val:g} {from_unit} {product} — это примерно {total_ml} мл (или ≈ {total_g} г).'

    # Уточнить целевую единицу
    if from_unit and not to_unit:
        return (
            f'Уточните, в чём хотите получить результат: в граммах или миллилитрах? '
            f'Например: «{qty_val:g} {from_unit} {product} — это сколько граммов?»'
        )

    return None


def _match_unit(s, patterns):
    """Находит единицу по строке s, используя список паттернов."""
    s = s.strip()
    for pat, norm_u in patterns:
        if re.search(pat, s):
            return norm_u
    return None



