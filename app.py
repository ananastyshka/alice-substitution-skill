# НОРМАЛИЗАЦИЯ ЗАПРОСА
# ============================================================
def normalize_query(text):
    text = text.lower().strip()
    text = re.sub(r'[,.!?;:«»"\'()]+', ' ', text)
    text = STOP_WORDS.sub(' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    tokens = [DECLENSION_MAP.get(t, t) for t in text.split()]
    return ' '.join(tokens)


