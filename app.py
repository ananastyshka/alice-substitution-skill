def extract_quantity(text):
    text_lower = text.lower()
    patterns = [
        (r'(\d+(?:[.,]\d+)?)\s*зубчик(?:а|ов|и)?', 'зубчик'),
        (r'(\d+(?:[.,]\d+)?)\s*луковиц(?:а|ы|е|у)?', 'луковица'),
        (r'(\d+(?:[.,]\d+)?)\s*головк(?:а|и|е|у)?', 'головка'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:г|гр|грамм(?:ов)?)\b', 'г'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:мл|миллилитр(?:ов)?)\b', 'мл'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:кг|килограмм(?:ов)?)\b', 'кг'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:л|литр(?:ов)?)\b', 'л'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:ст\.?\s*л\.?|столов\w*\s*ложк\w*)', 'ст.л.'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:ч\.?\s*л\.?|чайн\w*\s*ложк\w*)', 'ч.л.'),
        (r'(\d+(?:[.,]\d+)?)\s*стакан(?:а|ов)?', 'стакан'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:шт\.?|штук\w*)', 'шт.'),
    ]
    for pat, unit in patterns:
        m = re.search(pat, text_lower)
        if m:
            qty = float(m.group(1).replace(',', '.'))
            return qty, unit
    return None, None


