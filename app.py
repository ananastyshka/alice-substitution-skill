def convert_unit(qty, unit, ingredient_key):
    """Возвращает строку вида ' (≈ 75 г)' или ' (≈ 5 ст.л.)'.
    Для ключевых пар возвращает диапазон: ' (≈ 30–40 г)'."""
    if not qty or not unit:
        return ''
    tbl = DENSITY.get(ingredient_key, {})

    if unit in ('ст.л.', 'ч.л.', 'стакан', 'щепотка'):
        # Проверяем, есть ли диапазон
        range_key = (ingredient_key, unit)
        if range_key in UNIT_RANGES:
            lo, hi = UNIT_RANGES[range_key]
            total_lo = round(qty * lo)
            total_hi = round(qty * hi)
            return f' (≈ {total_lo}–{total_hi} г)'
        g_per = tbl.get(unit) or DEFAULT_UNIT_G.get(unit)
        if g_per:
            return f' (≈ {round(qty * g_per)} г)'

    if unit == 'г':
        g_per_tbsp = tbl.get('ст.л.')
        if g_per_tbsp:
            tbsp = round(qty / g_per_tbsp, 1)
            return f' (≈ {tbsp:g} ст.л.)'

    if unit == 'мл':
        g_per_ml = tbl.get('мл', 1.0)
        total_g  = round(qty * g_per_ml)
        tbsp     = round(qty / 15, 1)
        return f' (≈ {total_g} г / {tbsp:g} ст.л.)'

    return ''


