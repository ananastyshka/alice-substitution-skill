@app.route('/', methods=['POST'])
def handle_alice():
    data        = request.json or {}
    session     = data.get('session', {})
    version     = data.get('version', '1.0')
    session_id  = session.get('session_id', '')
    is_new      = session.get('new', False)
    user_input  = data.get('request', {}).get('original_utterance', '').strip()
    user_lower  = user_input.lower()

    # Новая сессия → приветствие
    if is_new:
        SESSION_CONTEXT.pop(session_id, None)
        return _reply(session, version, WELCOME_TEXT)

    if not user_input:
        return _reply(session, version, 'Повторите, пожалуйста.')

    # ИЗМЕНЕНИЕ: используем classify_intent (п.5)
    intent = classify_intent(user_input)

    if intent == 'greeting':
        SESSION_CONTEXT.pop(session_id, None)
        return _reply(session, version, WELCOME_TEXT)

    if intent == 'help':
        return _reply(session, version, HELP_TEXT)

    # ИЗМЕНЕНИЕ: обрабатываем конвертацию до поиска замен (п.1)
    if intent == 'conversion':
        conv_answer = handle_conversion(user_input)
        if conv_answer:
            return _reply(session, version, conv_answer)
        # Если handle_conversion вернул None — падаем в substitution

    # Substitution-ветка
    ctx             = SESSION_CONTEXT.get(session_id, {})
    last_ingredient = ctx.get('last_ingredient')

    ingredient_key  = find_ingredient(user_input)
    dish            = find_dish(user_input)
    qty, unit       = extract_quantity(user_input)

    if not ingredient_key:
        if last_ingredient and _looks_like_dish_clarification(user_input):
            ingredient_key = last_ingredient
            if not dish:
                dish = _guess_dish_from_short(user_input)
        else:
            return _reply(session, version, (
                'Не поняла, что вы хотите заменить. '
                'Назовите продукт, например: «масло», «яйцо», «мука», «соевый соус». '
                'Скажите «помощь», чтобы узнать все возможности.'
            ))

    SESSION_CONTEXT[session_id] = {
        'last_ingredient': ingredient_key,
        'last_dish': dish or ctx.get('last_dish'),
    }

    subs, subs_type = get_substitutions(ingredient_key, dish)
    answer = format_answer(ingredient_key, dish, subs, qty, unit, subs_type)
    return _reply(session, version, answer)


def _looks_like_dish_clarification(text):
    short = text.strip().lower()
    return len(short.split()) <= 5 and bool(re.search(r'\b(для|в|на|при)\b', short))


def _guess_dish_from_short(text):
    for d in DISH_KEYWORDS:
        if d in text.lower():
            return d
    return None


def _reply(session, version, text, end_session=False):
    return jsonify({
        'session': session,
        'version': version,
        'response': {'text': text, 'end_session': end_session},
    })


