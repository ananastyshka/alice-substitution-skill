@app.route('/', methods=['POST'])
def handle_alice():
    data = request.json
    session = data.get('session', {})
    user_input = data.get('request', {}).get('original_utterance', '').strip()
    is_new = session.get('new', False)

    if is_new or user_input.lower() in ['привет', 'старт', 'помощь', 'запусти навык']:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': 'Привет! Я кулинарный помощник. Назовите, что хотите заменить. Например: "чеснок", "2 зубчика чеснока", "мука пшеничная" или "чем заменить яйцо в кексе".',
                'end_session': False
            }
        })

    if not user_input:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {'text': 'Повторите, пожалуйста.', 'end_session': False}
        })

    # Нормализуем и ищем ингредиент
    ingredient_key = find_ingredient(user_input)
    if not ingredient_key:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': 'Не поняла, что вы хотите заменить. Попробуйте назвать продукт прямо: "чеснок", "мука", "яйцо".',
                'end_session': False
            }
        })

    dish = find_dish(user_input)
    qty, unit = extract_quantity(user_input)
    subs = get_substitutions(ingredient_key, dish)
    answer = format_answer(ingredient_key, dish, subs, qty, unit)

    return jsonify({
        'session': session,
        'version': data.get('version'),
        'response': {'text': answer, 'end_session': False}
    })

