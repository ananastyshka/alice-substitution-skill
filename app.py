def format_answer(ingredient, dish, subs, qty, unit):
    if not subs:
        return (
            f'Извините, я пока не знаю, чем заменить «{ingredient}». '
            f'Попробуйте спросить иначе или назовите другой продукт.'
        )
    header = f'Вот чем заменить {ingredient}'
    if dish:
        header += f' для блюда «{dish}»'
    if qty and unit:
        conv = convert_unit(qty, unit, ingredient)
        header += f' (на {qty:g} {unit}{conv})'
    return header + ':\n' + '\n'.join(f'• {s}' for s in subs)


