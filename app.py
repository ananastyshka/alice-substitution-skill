# ПОИСК ИНГРЕДИЕНТА
# ============================================================
def find_ingredient(user_input):
    norm = normalize_query(user_input)

    def resolve(key):
        seen = set()
        while isinstance(SUBSTITUTIONS_DB.get(key), str):
            if key in seen:
                break
            seen.add(key)
            key = SUBSTITUTIONS_DB[key]
        return key if isinstance(SUBSTITUTIONS_DB.get(key), list) else None

    # Сортируем ключи по убыванию длины — длинные совпадения приоритетнее
    for key in sorted(SUBSTITUTIONS_DB.keys(), key=len, reverse=True):
        if key in norm:
            resolved = resolve(key)
            if resolved:
                return resolved

    # Пробуем отдельные токены справа (ингредиент обычно в конце фразы)
    for token in reversed(norm.split()):
        if token in SUBSTITUTIONS_DB:
            resolved = resolve(token)
            if resolved:
                return resolved

    return None


