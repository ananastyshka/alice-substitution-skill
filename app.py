def normalize_text(text: str) -> str:
    """Приводит текст к нижнему регистру и удаляет знаки препинания."""
    text = text.lower().strip()
    text = re.sub(r'[^\w\s]', ' ', text)
    text = re.sub(r'\s+', ' ', text)
    return text

def extract_quantity(text: str) -> tuple[float | None, str | None]:
    """Извлекает количество и единицу измерения из запроса."""
    match = re.search(r'(\d+(?:[.,]\d+)?)\s*(гр|грамм|мл|ст\.?л\.?|ложк[аи]|ч\.?л\.?|кг|л|стакан|шт|штук)', text)
    if match:
        try:
            quantity = float(match.group(1).replace(',', '.'))
        except ValueError:
            quantity = None
        unit = match.group(2).lower()
        # Приводим к стандартным обозначениям
        if unit.startswith('ст'):
            unit = 'ст.л.'
        elif unit.startswith('ч'):
            unit = 'ч.л.'
        elif unit.startswith('гр'):
            unit = 'г'
        elif unit == 'грамм':
            unit = 'г'
        elif unit.startswith('мл'):
            unit = 'мл'
        elif unit == 'килограмм' or unit == 'кг':
            unit = 'кг'
        elif unit == 'литр' or unit == 'л':
            unit = 'л'
        return quantity, unit
    return None, None

def get_substitutions(ingredient: str, dish: str | None = None) -> list[str]:
    """Ищет замены для ингредиента, сначала специализированные, потом универсальные."""
    ingredient_normalized = normalize_text(ingredient)
    if dish:
        dish_normalized = normalize_text(dish)
    else:
        dish_normalized = ''

    # 1. Проверка специализированных замен (ингредиент + блюдо)
    for (key_ing, key_dish), substitutes in SPECIALIZED_SUBSTITUTIONS.items():
        if key_ing in ingredient_normalized and key_dish in dish_normalized:
            print(f"Найдена специализированная замена для '{key_ing}' в '{key_dish}'")
            return substitutes

    # 2. Поиск по универсальным заменам с нечетким совпадением
    possible_ingredients = get_close_matches(ingredient_normalized, UNIVERSAL_SUBSTITUTIONS.keys(), n=1, cutoff=0.6)
    if possible_ingredients:
        key = possible_ingredients[0]
        print(f"Найдена универсальная замена для '{key}'")
        return UNIVERSAL_SUBSTITUTIONS[key]

    return []

def find_dish_in_query(text: str) -> str | None:
    """Пытается извлечь название блюда из запроса."""
    text = text.lower()
    # Список ключевых слов для извлечения блюда
    patterns = [
        r'в\s+(\w+)',          # после предлога "в"
        r'для\s+(\w+)',        # после предлога "для"
        r'\b(\w+)\s*$'         # последнее слово
    ]
    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            return match.group(1)
    return None

@app.route('/', methods=['POST'])
def handle_alice():
    """Основная функция-обработчик запросов."""
    try:
        data = request.json
        session = data.get('session', {})
        user_input = data.get('request', {}).get('original_utterance', '').strip()
        is_new_session = session.get('new', False)
    except Exception as e:
        app.logger.error(f"Ошибка парсинга запроса: {e}")
        user_input = ""

    # Приветствие для новой сессии или команд помощи
    if is_new_session or user_input.lower() in ['привет', 'старт', 'помощь', 'запусти навык']:
        greeting = ("Привет! Я кулинарный помощник. "
                    "Назовите ингредиент, который хотите заменить, и я предложу варианты. "
                    "Например: «Чем заменить яйцо в кексе?», или «Яйцо печенье».")
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {'text': greeting, 'end_session': False},
            'session_state': {}
        })

    if not user_input:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': "Пожалуйста, назовите ингредиент. Например: «Заменить муку на что?» или «Яйцо в печенье».",
                'end_session': False
            },
            'session_state': {}
        })

    # Обработка запроса
    user_input_normalized = normalize_text(user_input)
    print(f"Обрабатываем запрос: {user_input_normalized}")

    # Извлекаем количество, если оно есть
    quantity, unit = extract_quantity(user_input_normalized)
    if quantity:
        print(f"Количество: {quantity} {unit}")

    # Поиск ингредиента
    found_ingredient = None
    for ingredient in UNIVERSAL_SUBSTITUTIONS.keys():
        if ingredient in user_input_normalized:
            found_ingredient = ingredient
            break

    if not found_ingredient:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': ("Не поняла, что вы хотите заменить. "
                         "Попробуйте сказать «замени яйцо» или «муку на что заменить?»."),
                'end_session': False
            },
            'session_state': {}
        })

    # Пытаемся найти блюдо
    dish = find_dish_in_query(user_input_normalized)
    if dish:
        print(f"Найдено блюдо: {dish}")

    # Поиск замен
    substitutions = get_substitutions(found_ingredient, dish)

    if not substitutions:
        # Если ничего не нашли
        response_text = (f"Для «{found_ingredient}» у меня пока нет точных рекомендаций. "
                         f"Попробуйте уточнить запрос, например «заменить {found_ingredient} в кексе».")
    else:
        # Формируем ответ
        header = f"Вот чем можно заменить {found_ingredient}"
        if dish:
            header += f" для блюда «{dish}»"
        if quantity and unit:
            header += f" (на {quantity} {unit})"
        response_text = header + ":\n\n" + "\n".join(f"• {sub}" for sub in substitutions)

    return jsonify({
        'session': session,
        'version': data.get('version'),
        'response': {'text': response_text, 'end_session': False},
        'session_state': {}
    })

