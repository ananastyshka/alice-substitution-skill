def convert_unit(qty, unit, ingredient_key):
    """Возвращает строку-конвертацию вроде ' (≈ 75 г)' или ' (≈ 5 ст.л.)'."""
    if not qty or not unit:
        return ''
    tbl = DENSITY.get(ingredient_key, {})

    if unit in ('ст.л.', 'ч.л.', 'стакан', 'щепотка'):
        g_per_unit = tbl.get(unit) or DEFAULT_UNIT_G.get(unit)
        if g_per_unit:
            return f' (≈ {round(qty * g_per_unit)} г)'

    if unit == 'г':
        g_per_tbsp = tbl.get('ст.л.')
        if g_per_tbsp:
            tbsp = round(qty / g_per_tbsp, 1)
            return f' (≈ {tbsp:g} ст.л.)'

    if unit == 'мл':
        g_per_ml = tbl.get('мл', 1.0)
        total_g = round(qty * g_per_ml)
        tbsp = round(qty / 15, 1)
        return f' (≈ {total_g} г / {tbsp:g} ст.л.)'

    return ''


