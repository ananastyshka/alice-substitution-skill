# 2. ФУНКЦИЯ ДЛЯ ИЗВЛЕЧЕНИЯ ДАННЫХ ИЗ ЗАПРОСА
# ----------------------------------------------------------------------
def extract_ingredient_and_dish(user_input):
    """
    Пытается найти в запросе ингредиент и блюдо.
    Возвращает кортеж (ингредиент, блюдо) или (None, None), если не удалось.
    """
    # Приводим к нижнему регистру для единообразия
    text = user_input.lower().strip()
    
    # 1. Пытаемся найти паттерн: "чего-то в чем-то"
    match = re.search(r'(.+?)(?:\s+в\s+|\s+для\s+)(.+)', text)
    if match:
        ingredient, dish = match.group(1).strip(), match.group(2).strip()
        # Удаляем явные слова-паразиты из ингредиента
        ingredient = re.sub(r'^(замени |нужно |найди |чем заменить| )', '', ingredient)
        ingredient = re.sub(r'\s+на$', '', ingredient) # удаляем предлог "на" в конце
        return ingredient, dish

    # 2. Если нет предлога, но есть два слова или больше, считаем ингредиентом всё, кроме последнего слова
    parts = text.split()
    if len(parts) >= 2:
        # Удаляем явные слова-паразиты из начала строки
        main_text = re.sub(r'^(замени |нужно |найди |чем заменить )', '', text)
        parts = main_text.split()
        if len(parts) >= 2:
            return ' '.join(parts[:-1]), parts[-1]
    
    # 3. Если ничего не подошло
    return None, None
