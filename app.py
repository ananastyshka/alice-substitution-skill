def normalize_query(text):
    text = text.lower().strip()
    text = re.sub(r'[,.!?;:«»"\'()]+', ' ', text)
    text = STOP_WORDS.sub(' ', text)
    text = re.sub(r'\s+', ' ', text).strip()
    tokens = [DECLENSION_MAP.get(t, t) for t in text.split()]
    return ' '.join(tokens)


# ============================================================
# ПОИСК ИНГРЕДИЕНТА
# ============================================================
def find_ingredient(user_input):
    norm = normalize_query(user_input)

    def resolve(key):
        seen = set()
        while isinstance(SUBSTITUTIONS_DB.get(key), str):
            if key in seen:
                break
            seen.add(key)
            key = SUBSTITUTIONS_DB[key]
        return key if isinstance(SUBSTITUTIONS_DB.get(key), list) else None

    for key in sorted(SUBSTITUTIONS_DB.keys(), key=len, reverse=True):
        if key in norm:
            resolved = resolve(key)
            if resolved:
                return resolved

    for token in reversed(norm.split()):
        if token in SUBSTITUTIONS_DB:
            resolved = resolve(token)
            if resolved:
                return resolved

    original_lower = user_input.lower()
    for key in sorted(SUBSTITUTIONS_DB.keys(), key=len, reverse=True):
        if key in original_lower:
            resolved = resolve(key)
            if resolved:
                return resolved

    return None


