# ИЗВЛЕЧЕНИЕ КОЛИЧЕСТВА
# ============================================================
def extract_quantity(text):
    text_lower = text.lower()

    if re.search(r'(?:пол|половин\w*)\s*стакан\w*', text_lower):
        return 0.5, 'стакан'
    if re.search(r'(?:четверть|четвёрть)\s*стакан\w*', text_lower):
        return 0.25, 'стакан'
    if re.search(r'(?:пол|половин\w*)\s*(?:чайн\w+\s+)?ложк\w+', text_lower):
        return 0.5, 'ч.л.'
    if re.search(r'щепотк\w+', text_lower):
        return 1.0, 'щепотка'
    if re.search(r'стакан\b(?!\w)', text_lower) and not re.search(r'\d', text_lower):
        return 1.0, 'стакан'

    patterns = [
        (r'(\d+(?:[.,]\d+)?)\s*зубчик(?:а|ов|и)?', 'зубчик'),
        (r'(\d+(?:[.,]\d+)?)\s*луковиц(?:а|ы|е|у)?', 'луковица'),
        (r'(\d+(?:[.,]\d+)?)\s*головк(?:а|и|е|у)?(?:\s+чеснока)?', 'головка'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:кг|килограмм(?:ов)?)\b', 'кг'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:л|литр(?:ов)?)\b', 'л'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:мл|миллилитр(?:ов)?)\b', 'мл'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:г|гр|грамм(?:ов)?)\b', 'г'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:ст\.?\s*л\.?|столов\w+\s+ложк\w+)', 'ст.л.'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:ч\.?\s*л\.?|чайн\w+\s+ложк\w+)', 'ч.л.'),
        (r'(\d+(?:[.,]\d+)?)\s*стакан(?:а|ов)?', 'стакан'),
        (r'(\d+(?:[.,]\d+)?)\s*(?:шт\.?|штук\w*)', 'шт.'),
    ]

    for pat, unit in patterns:
        m = re.search(pat, text_lower)
        if m:
            return float(m.group(1).replace(',', '.')), unit

    return None, None


