# КЛАССИФИКАЦИЯ НАМЕРЕНИЯ (п.5)
# ============================================================
def classify_intent(user_input):
    """
    Возвращает одно из: 'greeting', 'help', 'conversion', 'substitution'.
    """
    low = user_input.lower().strip()

    if low in ('привет', 'старт', 'запусти навык', 'начать'):
        return 'greeting'

    if HELP_TRIGGERS.search(low):
        return 'help'

    if CONVERSION_TRIGGERS.search(low):
        return 'conversion'

    return 'substitution'


