def find_dish(text):
    text_lower = text.lower()
    for d in sorted(DISH_KEYWORDS, key=len, reverse=True):
        if d in text_lower:
            return d
    m = re.search(r'(?:в|для|на)\s+([а-яё]{4,})', text_lower)
    if m:
        return m.group(1)
    return None


