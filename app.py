# 3. ОСНОВНАЯ ФУНКЦИЯ-ОБРАБОТЧИК
# ----------------------------------------------------------------------
@app.route('/', methods=['POST'])
def handle_alice():
    # --- Получаем и парсим запрос от Алисы ---
    data = request.json
    session = data.get('session', {})
    user_input = data.get('request', {}).get('original_utterance', '').strip()
    is_new_session = session.get('new', False)
    
    # --- Внутреннее состояние диалога (чтобы помнить, о чем говорили) ---
    # Данные хранятся в сессии и не теряются между сообщениями пользователя
    state = session.get('state', {})
    pending_ingredient = state.get('pending_ingredient')
    pending_dish = state.get('pending_dish')
    
    # --- Шаг 1: Приветствие ---
    if is_new_session or user_input.lower() in ['привет', 'старт', 'помощь', 'запусти навык']:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': (
                    'Здравствуйте! Я — Калькулятор замены продуктов. '
                    'Просто назовите ингредиент, который хотите заменить, и блюдо. '
                    'Например: "Сливочное масло для печенья" или "Чем заменить яйцо в кексе?"'
                ),
                'end_session': False
            },
            'session_state': {} # сбрасываем состояние, начинаем новый диалог
        })
    
    # Если пользователь не оставил свой запрос
    if not user_input:
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {
                'text': 'Извините, я вас не услышала. Повторите, пожалуйста, что вы хотите заменить.',
                'end_session': False
            },
            'session_state': state
        })
    
    # --- Шаг 2: Пытаемся извлечь ингредиент и блюдо из ТЕКУЩЕГО запроса ---
    ingredient, dish = extract_ingredient_and_dish(user_input)
    
    # --- Шаг 3: Логика уточнения, если данных не хватает ---
    # Ситуация А: У нас уже есть что-то на уточнении, а пользователь прислал новое сообщение
    if pending_ingredient and not pending_dish:
        # Мы ждали блюдо. Считаем, что пользователь назвал его сейчас.
        if not dish:
            dish = user_input
        ingredient = pending_ingredient
        pending_ingredient = None # сбрасываем, так как данные получены
    elif pending_dish and not pending_ingredient:
        # Мы ждали ингредиент. Считаем, что пользователь назвал его сейчас.
        if not ingredient:
            ingredient = user_input
        dish = pending_dish
        pending_dish = None
    # --- Конец логики уточнения ---
    
    # Проверяем, смогли ли мы распознать ингредиент и блюдо
    if not ingredient or not dish:
        # Если не смогли распознать оба параметра, уточняем
        if not ingredient and not dish:
            response_text = 'Не поняла. Назовите, что вы хотите заменить и в каком блюде. Например: "яйцо в блинах" или "муку для кекса".'
            new_state = {}
        elif ingredient and not dish:
            # Есть ингредиент, нет блюда. Запоминаем ингредиент и просим назвать блюдо.
            response_text = f'Поняла, ищем замену для "{ingredient}". А в каком блюде вы его используете?'
            new_state = {'pending_ingredient': ingredient}
        else: # not ingredient and dish:  # есть блюдо, нет ингредиента
            response_text = f'Поняла, речь идет о блюде "{dish}". А какой ингредиент вы хотите заменить?'
            new_state = {'pending_dish': dish}
        
        return jsonify({
            'session': session,
            'version': data.get('version'),
            'response': {'text': response_text, 'end_session': False},
            'session_state': new_state
        })
    
    # --- Шаг 4: Ура! Навык получил оба необходимых параметра. Ищем замену. ---
    # Очищаем ингредиент и блюдо от лишних пробелов и знаков препинания
    ingredient = ingredient.strip(' ,.!?')
    dish = dish.strip(' ,.!?')
    
    # Ищем точное совпадение в базе
    key = (ingredient, dish)
    replacements = RECIPES.get(key)
    
    if not replacements:
        # Если точного совпадения нет, пробуем найти частичное совпадение
        found = False
        for (k_ing, k_dish), value in RECIPES.items():
            if (k_ing in ingredient or ingredient in k_ing) and (k_dish in dish or dish in k_dish):
                replacements = value
                found = True
                break
        if not found:
            replacements = None
    
    # Формируем итоговый ответ
    if replacements:
        response_text = f'Вот чем можно заменить {ingredient} в {dish}:\n' + '\n'.join(f'• {r}' for r in replacements)
    else:
        response_text = f'Извините, пока не знаю, чем заменить "{ingredient}" в блюде "{dish}". Попробуйте переформулировать запрос или спросить про что-то другое.'
    
    return jsonify({
        'session': session,
        'version': data.get('version'),
        'response': {'text': response_text, 'end_session': False},
        'session_state': {} # после успешного ответа сбрасываем состояние
    })
