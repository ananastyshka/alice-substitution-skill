def format_answer(ingredient, dish, subs, qty, unit, subs_type='general'):
    if not subs:
        return (
            f'Извините, я пока не знаю, чем заменить «{ingredient}». '
            f'Попробуйте спросить иначе или назовите другой продукт.'
        )

    # ИЗМЕНЕНИЕ: структура ответа от частного к общему (п.3)
    if subs_type == 'special' and dish:
        # Есть специальный ответ для блюда
        header = f'Для блюда «{dish}» лучше всего подойдёт:'
        if qty and unit:
            conv = convert_unit(qty, unit, ingredient)
            header = (
                f'Для блюда «{dish}» (на {qty:g} {unit}{conv}) '
                f'лучше всего подойдёт:'
            )
        return header + '\n' + '\n'.join(f'• {s}' for s in subs)
    else:
        # Общий ответ — разбиваем по контексту если есть блюдо
        if dish:
            # Разбиваем замены: сначала те, что упоминают блюдо, потом остальные
            specific = [s for s in subs if dish in s.lower()]
            universal = [s for s in subs if dish not in s.lower()]

            parts = []
            if specific:
                parts.append(
                    f'Для блюда «{dish}»:\n' +
                    '\n'.join(f'• {s}' for s in specific)
                )
            if universal:
                ctx_label = 'Универсальные варианты' if specific else f'Чем заменить {ingredient}'
                parts.append(
                    ctx_label + ':\n' +
                    '\n'.join(f'• {s}' for s in universal)
                )
            result = '\n\n'.join(parts)
        else:
            header = f'Вот чем заменить {ingredient}'
            if qty and unit:
                conv = convert_unit(qty, unit, ingredient)
                header += f' (на {qty:g} {unit}{conv})'
            result = header + ':\n' + '\n'.join(f'• {s}' for s in subs)

        return result


