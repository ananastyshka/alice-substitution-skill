def normalize_name(name):
    """Приводит ингредиент к каноническому виду, убирает лишнее."""
    name = name.lower().strip()
    # Удаляем предлоги и слова-паразиты
    name = re.sub(r'\b(замени|чем|можно|нужно|как|для|в|на|или|и|пожалуйста)\b', '', name)
    name = re.sub(r'\s+', ' ', name)
    return name.strip()

def find_ingredient(user_input):
    """Ищет ингредиент в базе (прямое вхождение или синоним)."""
    user_norm = normalize_name(user_input)
    # Сначала ищем точное совпадение в ключах
    for key in SUBSTITUTIONS_DB:
        if key in user_norm:
            # Если это синоним (значение строка), то переходим к основному ключу
            while isinstance(SUBSTITUTIONS_DB[key], str):
                key = SUBSTITUTIONS_DB[key]
            return key
    # Если не нашли, пробуем по отдельным словам (последнее слово может быть ингредиентом)
    words = user_norm.split()
    for word in words:
        if word in SUBSTITUTIONS_DB:
            w = SUBSTITUTIONS_DB[word]
            while isinstance(w, str):
                w = SUBSTITUTIONS_DB[w]
            return w
    return None

def find_dish(text):
    """Извлекает блюдо из запроса."""
    # Ищем после "в", "для", "на"
    match = re.search(r'(?:в|для|на)\s+(\w+)', text.lower())
    if match:
        return match.group(1)
    # Если есть слово "блины", "печенье", "кекс" и т.д.
    dish_keywords = ['печенье', 'кекс', 'блины', 'оладьи', 'пирог', 'торт', 'соус', 'котлеты', 'суп']
    for d in dish_keywords:
        if d in text.lower():
            return d
    return None

def get_substitutions(ingredient_key, dish=None):
    """Возвращает список замен для данного ингредиента и (опционально) блюда."""
    # Проверка специальных комбинаций
    if dish:
        for (k_ing, k_dish), subs in SPECIAL.items():
            if k_ing in ingredient_key and k_dish in dish:
                return subs
    # Обычные замены из базы
    subs = SUBSTITUTIONS_DB.get(ingredient_key, [])
    if subs and isinstance(subs, list):
        return subs
    return []

def extract_quantity(text):
    """Выдёргивает количество и единицу."""
    patterns = [
        r'(\d+(?:[.,]\d+)?)\s*(зубчик[а]?|зубчика|зубчиков)',
        r'(\d+(?:[.,]\d+)?)\s*(г|гр|грамм|мл|ст\.?л\.?|ч\.?л\.?|кг|л|стакан|шт|штук|головк[аи]|луковиц[аы])'
    ]
    for pat in patterns:
        m = re.search(pat, text.lower())
        if m:
            q = float(m.group(1).replace(',', '.'))
            u = m.group(2).lower()
            if u.startswith('гр') or u == 'грамм':
                u = 'г'
            elif u.startswith('ст'):
                u = 'ст.л.'
            elif u.startswith('ч'):
                u = 'ч.л.'
            elif u.startswith('зубчик'):
                u = 'зубчик'
            elif u.startswith('головк'):
                u = 'головка'
            return q, u
    return None, None

def format_answer(ingredient, dish, subs, qty, unit):
    if not subs:
        return f"Извините, я пока не знаю, чем заменить «{ingredient}». Попробуйте спросить иначе."
    header = f"Вот чем можно заменить {ingredient}"
    if dish:
        header += f" для блюда «{dish}»"
    if qty and unit:
        header += f" (на {qty} {unit})"
    return header + ":\n" + "\n".join(f"• {s}" for s in subs)

