def get_substitutions(ingredient_key, dish=None):
    # ИЗМЕНЕНИЕ: логика «от частного к общему» (п.3)
    # 1) Сначала ищем прямое совпадение «ингредиент + блюдо»
    if dish:
        for (k_ing, k_dish), subs in SPECIAL.items():
            if k_ing in ingredient_key and k_dish in dish:
                return subs, 'special'

    # 2) Если прямого совпадения нет — берём все замены из базы
    subs = SUBSTITUTIONS_DB.get(ingredient_key, [])
    return (subs if isinstance(subs, list) else []), 'general'


